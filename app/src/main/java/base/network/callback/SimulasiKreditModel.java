public class SimulasiKreditModel {

    private String dana;
    private String Agunan;
    private String totalBiaya;
    private String tahunKendaraan;
    private String areaTT;
    private String kotaTerdekat;
    private String totalHarga;
    private String uangMuka;

    public String getDana() {
        return dana;
    }

    public void setDana(String dana) {
        this.dana = dana;
    }

    public String getAgunan() {
        return Agunan;
    }

    public void setAgunan(String agunan) {
        Agunan = agunan;
    }

    public String getTotalBiaya() {
        return totalBiaya;
    }

    public void setTotalBiaya(String totalBiaya) {
        this.totalBiaya = totalBiaya;
    }

    public String getTahunKendaraan() {
        return tahunKendaraan;
    }

    public void setTahunKendaraan(String tahunKendaraan) {
        this.tahunKendaraan = tahunKendaraan;
    }

    public String getAreaTT() {
        return areaTT;
    }

    public void setAreaTT(String areaTT) {
        this.areaTT = areaTT;
    }

    public String getKotaTerdekat() {
        return kotaTerdekat;
    }

    public void setKotaTerdekat(String kotaTerdekat) {
        this.kotaTerdekat = kotaTerdekat;
    }

    public String getTotalHarga() {
        return totalHarga;
    }

    public void setTotalHarga(String totalHarga) {
        this.totalHarga = totalHarga;
    }

    public String getUangMuka() {
        return uangMuka;
    }

    public void setUangMuka(String uangMuka) {
        this.uangMuka = uangMuka;
    }
}
